// Generate prisma/schema.prod.prisma from the committed SQLite schema, with the
// datasource provider switched to PostgreSQL — for production deploys (Vercel +
// hosted Postgres). SQLite stays the committed default so local dev, tests, and
// the zero-config demo keep working. This is generated (gitignored) so the two
// schemas can never drift: prisma/schema.prisma is the single source of truth.
//
// The data model is already provider-agnostic (enums are stored as String,
// ids are cuid(), all types are portable), so a pure provider swap is safe.

import { readFileSync, writeFileSync } from "node:fs";

const SRC = "prisma/schema.prisma";
const OUT = "prisma/schema.prod.prisma";

const src = readFileSync(SRC, "utf8");
const swapped = src.replace(/provider\s*=\s*"sqlite"/, 'provider = "postgresql"');

if (swapped === src) {
  console.error(`[build-prisma-schema] could not find provider="sqlite" in ${SRC}`);
  process.exit(1);
}

const banner =
  "// GENERATED by scripts/build-prisma-schema.mjs — DO NOT EDIT.\n" +
  "// Source of truth: prisma/schema.prisma (SQLite). This is the PostgreSQL\n" +
  "// variant used for production (Vercel + hosted Postgres).\n\n";

writeFileSync(OUT, banner + swapped);
console.log(`[build-prisma-schema] wrote ${OUT} (provider=postgresql)`);
